let btn1 = false;
function val1Btn_onClick(){
    let arr = document.getElementById("plus1");
    if (!btn1) {
        arr.style.opacity = "0";
        // arr.style.transition = "opacity 0.5s easy";
        btn1 = true;
    }
    else {
        btn1 = false;
        arr.style.opacity = "1";
        // arr.style.transition = "opacity 0.5s easy";
    }
}


